# 4. Use a Virtual Environment:

# For better project isolation, consider using virtual environments. 

# # Create Vertual Enviornment

# Create a virtual environment using 

print("python -m venv myenv")

# #where `myenv` is the name of your virtual environment.

# # Activate Virtual Environment
# To activate the virtual environment, use the following command:

print(".\\myenv\\Scripts\\activate")

# That's It
# # You can now install packages and run your Python scripts within this isolated environment.

